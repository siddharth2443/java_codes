class Core {
	public static void main(String[] args) {
	
		int ch=65;
		switch(ch) {
		
			case 'A' :
				System.out.println("char-A");
				break;
			case 6 :
				System.out.println("num-65");
				break;
			case 'B' :
				System.out.println("char-B");
				break;
			case 21 :
				System.out.println("num-66");
				break;

			default :
				System.out.println("default");
		}
	}
}
