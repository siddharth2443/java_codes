class Core {
	public static void main(String[] args) {
	
		String str="mon";

		switch(str) {
		
			case "mon" :
				System.out.println("Monday");
				break;
			case "tue" :
				System.out.println("Tuesday");
				break;
			case "wed" :
				System.out.println("Wednesday");
				break;
			default :
				System.out.println("default");
				break;
		}
	}
}
